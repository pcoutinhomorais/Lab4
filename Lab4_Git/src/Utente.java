
public class Utente {
private String nome;
private String genero;
private int idade;
private float altura;
private int peso;
/**
 * @param nome
 * @param genero
 * @param idade
 * @param altura
 * @param peso
 */
public Utente(String nome, String genero, int idade, float altura, int peso) {
	super();
	this.nome = nome;
	this.genero = genero;
	this.idade = idade;
	this.altura = altura;
	this.peso = peso;
}

}
